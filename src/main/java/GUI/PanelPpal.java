    private void b_modificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_b_modificarActionPerformed
        int[] filas = tabla.getSelectedRows();

        if (filas.length > 0 && filas.length < 2) {
            try {
                Perro p = new Perro();
                
                p.setId(Integer.parseInt((String)modelo.getValueAt(filas[0], 0)));
                p.setNombre((String) modelo.getValueAt(filas[0], 1));
                p.setEdad(Integer.parseInt((String)modelo.getValueAt(filas[0], 2)));
                p.setRaza((String)modelo.getValueAt(filas[0], 3));
                
                new VInsertarOMod(this,c, false, p).setVisible(true);
            } catch (Exception ex) {
                Logger.getLogger(PanelPpal.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            if (filas.length <= 0) {
                JOptionPane.showMessageDialog(this, "Debes seleccionar una fila");
            } else if (filas.length > 1) {
                JOptionPane.showMessageDialog(this, "Solo puedes seleccionar una fila");
            }
        }

    }//GEN-LAST:event_b_modificarActionPerformed

    private void b_borrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_b_borrarActionPerformed
        int[] filas = tabla.getSelectedRows();
        if (filas.length > 0) {
            Integer[] filasOrdenadas = new Integer[filas.length];
            for (int i = 0; i < filasOrdenadas.length; i++) {
                filasOrdenadas[i] = filas[i];
            }
            Arrays.sort(filasOrdenadas, Collections.reverseOrder());
            for (int i = 0; i < filasOrdenadas.length; i++) {
                try {
                    Perro p = new Perro();
                    p.setId(Integer.parseInt((String) modelo.getValueAt(filas[i], 0)));
                    c.borrarPerro(p);
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(this, ex.getMessage());
                }
            }
            cargarTabla(c.listaPerros());

        } else {
                JOptionPane.showMessageDialog(this, "Debes seleccionar una fila");
        }
    }//GEN-LAST:event_b_borrarActionPerformed

