    private void minitComponets() {
        camposDeLaTabla();
        cargarTabla(c.listaPerros());
    }

    private void camposDeLaTabla() {
        String[] campos = {"Id_Perro", "Nombre", "Edad", "Raza"};
        modelo = new DefaultTableModel(campos, 0) {
            public boolean isCellEditable(int rowIndex, int vColIndex) {
                return false;
            }
        };
        tabla.setModel(modelo);
    }
    public void cargarTabla(List<Perro> perros){
        limpiarTabla();
        for (Perro p : perros) {
            String[] fila={p.getId()+"",p.getNombre(),p.getEdad()+"",p.getRaza()};
            modelo.addRow(fila);
        }
    }
    private void limpiarTabla(){
        int filas=tabla.getRowCount();
        for (int i = filas-1; i >=0; i--) {
            modelo.removeRow(i);  
        }
    }
    public DefaultTableModel getModelo() {
        return modelo;
    }

    public JTable getTabla() {
        return tabla;
    }
    
